void main() {
  int variable = 4;

  if (variable == 0) {
    print("Zero");
  } else if (variable == 1) {
    print("One");
  } else if (variable == 2) {
    print("Two");
  } else if (variable == 3) {
    print("Three");
  } else if (variable == 4) {
    print("Four");
  } else if (variable == 5) {
    print("Five");
  } else {
    print("The number is greater than 5.");
  }
}
